#!/usr/bin/env bun

import { denyPreToolUse } from "./hook-utils.ts"

interface HookInput {
  tool_name: string
  tool_input?: {
    file_path?: string
  }
}

// Matches any lockfile at path boundaries (handles / and \ separators, case-insensitive)
const LOCKFILE_RE =
  /(^|[\\/])(pnpm-lock\.yaml|package-lock\.json|npm-shrinkwrap\.json|yarn\.lock|bun\.lockb?|shrinkwrap\.yaml)$/i

async function main() {
  const input: HookInput = await Bun.stdin.json()

  const filePath = input.tool_input?.file_path ?? ""

  if (LOCKFILE_RE.test(filePath)) {
    const reason = [
      "You cannot edit lockfiles directly.",
      "",
      "Lockfiles (pnpm-lock.yaml, package-lock.json, yarn.lock, bun.lock, etc.) are",
      "machine-generated by the package manager. Any manual edit will be overwritten",
      "the next time dependencies are installed or updated, and can silently corrupt",
      "the dependency graph.",
      "",
      "To update a lockfile, run the appropriate package manager command instead:",
      "  bun install          # bun.lock / bun.lockb",
      "  pnpm install         # pnpm-lock.yaml",
      "  npm install          # package-lock.json / npm-shrinkwrap.json",
      "  yarn install         # yarn.lock",
      "",
      "Do not edit lockfiles directly.",
    ].join("\n")

    denyPreToolUse(reason)
  }

  // Allow the edit
  console.log(
    JSON.stringify({
      hookSpecificOutput: {
        hookEventName: "PreToolUse",
        permissionDecision: "allow",
      },
    })
  )
}

main().catch((e) => {
  console.error("Hook error:", e)
  process.exit(1)
})
